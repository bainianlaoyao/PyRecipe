from 菜谱框架 import *

# 定义番茄炒蛋 - 简化版本
def 番茄炒蛋_简化(菜: 菜谱构建器):
    """番茄炒蛋 - 简化版"""
    番茄 = 菜.切(菜.取食材("番茄", 个(2)), "块")
    鸡蛋 = 菜.取食材("鸡蛋", 个(3))
    葱 = 菜.切(菜.取食材("葱", 根(1)), "段")
    盐 = 菜.取食材("盐", 克(3))

    炒蛋 = 菜.炒([鸡蛋], 时间_秒=30, 描述="炒", 名称="炒鸡蛋", 用油=10)
    炒番茄 = 菜.炒([番茄], 时间_秒=20, 描述="炒", 名称="炒番茄", 用油=5)
    混合 = 菜.混合([炒蛋, 炒番茄], "翻炒均匀")
    最终 = 菜.调味([混合, 葱, 盐], "加入盐和葱花，炒匀")

    return 最终

